public class DataTypes {
    public static void main(String [] args) {
        //Format example:
        //Define data type (string), 
        //then define variable name (phrase), 
        //then assign value to variable ( = "To be or not to be"), 
        //close code to be executed with (;)
        
        // String - String (string of words or other stuff)
        String phrase = "To be or not to be";
        System.out.println(phrase);
        
        // int - Integer (whole numbers)
        int age = 35;
        System.out.println(age);
        
        // double - Double (number with decimal)
        double gpa = 3.4;
        System.out.println(gpa);
        
        // char - Character (single character inside single quotes)
        char grade = 'A';
        System.out.println(grade);
        
        // boolean - boolean (true or false)
        boolean isMale = false;
        System.out.println(isMale);
    }
}